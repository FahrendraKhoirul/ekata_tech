            <li>Aplikasi yang dibangun dengan Flutter memiliki performa native yang tinggi, baik di Android maupun iOS.</li>
            <li>Flutter memiliki komunitas yang kuat dan terus berkembang. Sebagai contoh, terdapat Flutter Awesome yang berisi daftar library dan tools terbaik. Ada pula Flutter Community, yaitu publikasi Medium yang berisi artikel, tutorial, dll.</li>
          </ul>
