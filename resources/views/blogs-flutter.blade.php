          <p>Halo teman-teman...</p>
          <p>Teman-teman tahu tidak? Flutter telah booming sejak pertengahan 2019. Top apps di Play Store, dari Google Ads hingga Alibaba, telah menerapkannya. Flutter sendiri merupakan teknologi milik Google yang digunakan untuk membangun aplikasi dengan UI yang api, serta dapat di-compile secara native ke dalam aplikasi mobile, web, dan desktop hanya dari satu basis kode. Flutter menggunakan bahasa <a href="https://www.google.com/url?q=https://dart.dev/&sa=D&ust=1587033113223000">Dart</a> sebuah bahasa pemrograman yang dikembangkan oleh Google.</p>
          <p>Versi pertama Flutter dikenal sebagai "Sky" dan berjalan pada sistem operasi <a href="https://id.wikipedia.org/wiki/Android_(sistem_operasi">Android</a>. Diresmikan pada perhelatan Dart developer summit tahun 2015, dengan tujuan untuk mampu merender grafis secara konsisten pada 120 bingkai per detik.
            Flutter 1.0 dirilis pada tanggal 4 Desember 2018 di acara Flutter Live, yang menunjukkan versi "stabil" pertama dari Framework Flutter.
          </p>
          <p class="fs-4 fw-bold">Kenapa harus Flutter?</p>
          <ol>
            <li>Cross Platform</li>
            <p>Flutter mendukung cross platform, artinya flutter dapat dijalankan di beberapa platform yang berbeda. kita dapat membangun aplikasi Android dan IOS sekaligus dengan Flutter ini. Tidak hanya itu, kita juga dapat membangun aplikasi web dan desktop. Dengan kelebihan ini, kita dapat menghemat waktu dalam pembuatannya. kita tdak perlu mempelajari masing masing bahasa native yang digunakan. Dengan Flutter kita dapat membuat semua aplikasi di masing masing platform.</p>
            <li>Fast Development (Hot Reload)</li>
            <p>Flutter memiliki sebuah fitur bernama <a href="https://www.google.com/url?q=https://flutter.dev/docs/development/tools/hot-reload&sa=D&ust=1587033113226000">hot reload</a>. Dengan fitur ini, proses pengembangan aplikasi dapat berjalan lebih cepat dan mudah.. Setelah melakukan perubahan pada kode program, cukup tekan hot reload. Aplikasi akan diperbarui dalam kurun waktu kurang dari 1 detik. Sangat cepat bukan?
            </p>
            <p>Hot reload bekerja dengan cara menginjeksi kode program yang mengalami perubahan ke dalam Dart Virtual Machine. Setelah virtual machine memperbarui tiap kelas dengan kode program versi terbaru, maka framework Flutter secara otomatis membangun kembali susunan komponen widget sehingga kita dapat dengan cepat melihat perubahan yang terjadi.</p>
            <li>Beautiful UI</li>
            <p>Flutter dirancang untuk mempermudah developer dalam membangun tampilan user interface. Keseluruhan UI pada Flutter dibangun menggunakan <a href="https://www.google.com/url?q=https://flutter.dev/docs/development/ui/widgets-intro&sa=D&ust=1587033113228000">widget</a>.

              Saat ini Flutter memmiliki 3 set widget, <a href="https://www.google.com/url?q=https://flutter.dev/docs/development/ui/widgets/material&sa=D&ust=1587033113229000">Material Design</a> (Android), <a href="https://www.google.com/url?q=https://flutter.dev/docs/development/ui/widgets/cupertino&sa=D&ust=1587033113229000">Cupertino</a> (iOS) dan juga Fluent (Windows).Teman-teman dapat melihat daftar lengkap widget yang tersedia <a href="(https://www.google.com/url?q=https://flutter.dev/docs/development/ui/widgets&sa=D&ust=1587033113229000">di tautan ini</a>. Selain itu, di tiap pekan, tim Flutter juga membahas Widget of the Week yang dapat teman-teman saksikan lewat channel <a href="https://www.google.com/url?q=https://www.youtube.com/channel/UCwXdFgeE9KYzlDdR7TG9cMw&sa=D&ust=1587033113230000">Youtube Flutter</a>.</p>
          </ol>
          <p>Selain 3 poin diatas, ada beberapa alasan lain kenapa Flutter wajib untuk teman-teman pelajari:</p>
